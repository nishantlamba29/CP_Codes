    int x=0,y=0,z=0;
    
    for(int i=0;i<n;i++){
        if(s[i]=='+') x++;
        else y++;
        fq[i][0] = x;
        fq[i][1] = y;
    }

    for(int i=1;i<n;i++){
        if(s[i-1]=='-' && s[i]=='-') {
            z++;
            fq[i][2] = z;
            i++;
        }
        if(i<n) fq[i][2] = z;
    }
    // for(int i=0;i<n;i++){
