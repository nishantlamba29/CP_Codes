    // vn = n.(10nk - 1)/(10k - 1)
    // vn = n.(10k - 1)(mod-2)*(10nk - 1)%mod
    // vn = n.t1.t2
    int t1 = powr(powr(10,k) - 1, mod - 2);
    int t2 = powr(powr(10,n),k) - 1;
