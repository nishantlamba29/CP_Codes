#define int long long

const int mod = 998244353;

int powr(int a, int b, int k = mod){
    int res = 1;
    while(b>0){
        if(b&1) res = (res*a)%k;
        a = (a*a)%k;
        b = b>>1;
    }
    return res%k;
}

