const int mod = 1e9+7;
int dp[100005];

int rec(int n, int k){
    if(n <= 0) return 0;
    if(dp[n] != -1) return dp[n];
    int ff = (1+ rec(n-1,k))% mod;
    int ss = 0;
    if(n >= k)
    ss = (1 + rec(n-k, k)) % mod;

    return dp[n] = (ff + ss) % mod;
}
