int rec(int n, int m, int i, int j, int k, string s, string t, vector<vector<vector<int>>> &dp){
    if(j==m){
        if(i==n) return 0;
        else return (k==1);
    }
    if(i == n) return inf;
    if(dp[i][j][k] != -1) return dp[i][j][k];
    if(s[i]!=t[j]){
        int ans = rec(n, m, i+1, j, 0, s, t, dp);
        return dp[i][j][k] = ans + (k==1);
    }
    
    int op1 = rec(n, m, i+1, j, 0, s, t, dp) + (k==1);
    int op2 = rec(n, m, i+1, j+1, 1, s, t, dp);
    
    return dp[i][j][k] = min(op1, op2);
}
