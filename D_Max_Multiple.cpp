#include<bits/stdc++.h>
using namespace std;
// Consistency doesn't guarantee you will be successful
// but not being consistent will guarantee that you won't reach success.
#define int long long
int32_t main(){
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);
    int n,k,d;
    cin>>n>>k>>d;
    vector<int> v(n);
    for(int i=0;i<n;i++) cin>>v[i];
    
    vector<vector<vector<int>>> dp(n+1, vector<vector<int>>(k+1, vector<int>(d+1, -1)));
    dp[0][0][0] = 0;
    for(int i=0;i<n;i++){
        for(int j=0;j<=k;j++){
            for(int l=0;l<d;l++){
                if(dp[i][j][l] != -1){
                    dp[i+1][j][l] = max(dp[i+1][j][l], dp[i][j][l]);
                    if(j < k)
                        dp[i+1][j+1][(l+v[i])%d] = max(dp[i][j][l] + v[i], dp[i+1][j+1][(l+v[i])%d] );
                }
            }
        }
    }
    cout<<dp[n][k][0]<<"\n";
    return 0;
}
